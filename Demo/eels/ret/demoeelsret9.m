%  DEMOEELSRET9 - EEL spectra for coupled nanorods using iterative BEM solver.
%    For coupled silver nanorods this program computes the energy loss
%    probability for selected impact parameters and loss energies using the
%    full Maxwell equations and an iterative BEM solver.
%
%  Runtime on my computer:  60 min.

%%  initialization
%  options for BEM simulation
op = bemoptions( 'sim', 'ret', 'interp', 'curv' );
op.iter = bemiter.options;
%  table of dielectric functions
epstab  = { epsconst( 1 ), epstable( 'silver.dat' ), epstable( 'silver.dat' ) };

%  dimensions of particle
len = [ 1000, 70 ];
%  polygon
poly = round( polygon( 4, 'size', len ), 'rad', 5 ); 
%  edge profile
edge = edgeprofile( 30, 11 );

%  hdata
hdata = struct( 'hmax', 15 );
%  extrude polygon
p1 = shift( tripolygon( poly, edge, 'hdata', hdata ), [ - 510, 0, 0 ] );
p2 = shift( p1, [ 1020, 0, 0 ] );

%  make particle
p = comparticle( epstab, { p1, p2 }, [ 2, 1; 3, 1 ], 1, 2, op );

%  width of electron beam and electron velocity 
[ width, vel ] = deal( 0.2, eelsbase.ene2vel( 200e3 ) );
%  impact parameters (moving away from gap)
imp = [ 0, 0; 50, 0; 100, 0; 200, 0 ];
% %  plot particle and impact parameters
figure
 plot( p, 'EdgeColor', 'b' );  hold on
% plot3( imp( :, 1 ), imp( :, 2 ), 0 * imp( :, 1 ) + 30, 'm+' );

%  loss energies in eV
ene = linspace( 0.7, 1.5, 20 );
%  convert energies to nm
units;  enei = eV2nm ./ ene;

%%  BEM simulation
%  BEM solver
bem = bemsolver( p, op );
%  electron beam excitation
exc = electronbeam( p, imp, width, vel, op );

%  surface and bulk loss
[ psurf, pbulk ] = deal( zeros( size( imp, 1 ), numel( enei ) ) );

multiWaitbar( 'BEM solver', 0, 'Color', 'g', 'CanCancel', 'on' );
%  loop over energies
for ien = 1 : length( ene )
  %  surface charges
  sig = bem \ exc( enei( ien ) );
  %  EELS losses
  [ psurf( :, ien ), pbulk( :, ien ) ] = exc.loss( sig );
  
  multiWaitbar( 'BEM solver', ien / numel( enei ) );
end
%  close waitbar
multiWaitbar( 'CloseAll' );

%%  final plot
figure
plot( ene, psurf + pbulk, 'o' );  hold on

legend( 'gap', '50 nm', '100 nm', '200 nm' );

xlabel( 'Loss energy (eV)' );
ylabel( 'Loss probability (eV^{-1})' );

